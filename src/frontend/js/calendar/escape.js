// HTMLエスケープ関数
export function escapeHtml(text) {
            if (text === null || text === undefined) {
                return "";
            }
            return String(text)
                .replace(/&/g, "&amp;")
                .replace(/</g, "&lt;")
                .replace(/>/g, "&gt;")
                .replace(/"/g, "&quot;")
                .replace(/'/g, "&#039;");
}